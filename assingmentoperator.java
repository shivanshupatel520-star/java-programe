public class assingmentoperator {
    public static void main(String[] args) {
        int p = 10;
        int q;

//        =operator
        q=p;
        System.out.println(q);

//        +=operator
        p+=q;
        System.out.println(p);

//        -=operator
        p-=q;
        System.out.println(p);

        p*=q;
        System.out.println(p);

        p/=q;
        System.out.println(p);
    }
}
